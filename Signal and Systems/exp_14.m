n=0:1:10;
h=sin(.5*n);
x=sin(.2*n);
y=conv(h,x);
y1=length(y);
n1=0:1:y1-1;
subplot(3,1,1);
stem(n,h);
xlabel('Time');
ylabel('Amplitude');
title('h(n)');
subplot(3,1,2);
stem(n,x);
xlabel('Time');
ylabel('Amplitude');
title('x(n)');
subplot(3,1,3);
stem(n1,y);
xlabel('Time');
ylabel('Amplitude');
title('Convolution of the two signals');
